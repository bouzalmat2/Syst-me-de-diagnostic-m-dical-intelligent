import React, { useState } from 'react';
import { motion } from 'framer-motion';
import {
  FaCalendarAlt,
  FaUserMd,
  FaNotesMedical,
  FaFileMedical,
  FaFilter,
} from 'react-icons/fa';
import PatientLayout from '../../components/Patient/PatientLayout';
import './PatientPages.css';

const PatientHistory = () => {
  const [filter, setFilter] = useState('all');

  const medicalHistory = [
    {
      id: 1,
      type: 'appointment',
      date: '2026-01-15',
      doctor: 'Dr. Sarah Johnson',
      title: 'Annual Checkup',
      notes: 'Patient is in good health. Blood pressure normal. Recommended annual flu shot.',
      documents: ['Lab Report.pdf', 'Prescription.pdf'],
    },
    {
      id: 2,
      type: 'prescription',
      date: '2026-01-10',
      doctor: 'Dr. Michael Chen',
      title: 'Medication Prescribed',
      notes: 'Amoxicillin 500mg - Take 3 times daily for 7 days',
      documents: ['Prescription.pdf'],
    },
    {
      id: 3,
      type: 'lab',
      date: '2025-12-20',
      doctor: 'Dr. Emily Brown',
      title: 'Blood Test Results',
      notes: 'Complete blood count shows normal results. All values within reference range.',
      documents: ['Blood Test Results.pdf'],
    },
    {
      id: 4,
      type: 'appointment',
      date: '2025-12-05',
      doctor: 'Dr. James Wilson',
      title: 'Follow-up Consultation',
      notes: 'Recovery progressing well. Continue current medication.',
      documents: [],
    },
  ];

  const filteredHistory =
    filter === 'all'
      ? medicalHistory
      : medicalHistory.filter((item) => item.type === filter);

  const getTypeIcon = (type) => {
    switch (type) {
      case 'appointment':
        return <FaCalendarAlt />;
      case 'prescription':
        return <FaNotesMedical />;
      case 'lab':
        return <FaFileMedical />;
      default:
        return <FaUserMd />;
    }
  };

  const getTypeLabel = (type) => {
    switch (type) {
      case 'appointment':
        return 'Appointment';
      case 'prescription':
        return 'Prescription';
      case 'lab':
        return 'Lab Result';
      default:
        return 'Medical Record';
    }
  };

  return (
    <PatientLayout>
      <div className="page-container">
        <div className="page-header">
          <div>
            <h1 className="page-title">Medical History</h1>
            <p className="page-subtitle">View your complete health records</p>
          </div>
        </div>

        <div className="filter-bar">
          <FaFilter className="filter-icon" />
          <div className="filter-buttons">
            <button
              className={`filter-btn ${filter === 'all' ? 'active' : ''}`}
              onClick={() => setFilter('all')}
            >
              All Records
            </button>
            <button
              className={`filter-btn ${filter === 'appointment' ? 'active' : ''}`}
              onClick={() => setFilter('appointment')}
            >
              Appointments
            </button>
            <button
              className={`filter-btn ${filter === 'prescription' ? 'active' : ''}`}
              onClick={() => setFilter('prescription')}
            >
              Prescriptions
            </button>
            <button
              className={`filter-btn ${filter === 'lab' ? 'active' : ''}`}
              onClick={() => setFilter('lab')}
            >
              Lab Results
            </button>
          </div>
        </div>

        <div className="history-timeline">
          {filteredHistory.map((record, index) => (
            <motion.div
              key={record.id}
              initial={{ opacity: 0, x: -20 }}
              animate={{ opacity: 1, x: 0 }}
              transition={{ delay: index * 0.1 }}
              className="history-item"
            >
              <div className="history-date">
                <span>{record.date}</span>
              </div>
              <div className="history-icon">
                {getTypeIcon(record.type)}
              </div>
              <div className="history-content">
                <div className="history-header">
                  <div>
                    <span className="history-type">{getTypeLabel(record.type)}</span>
                    <h3 className="history-title">{record.title}</h3>
                    <p className="history-doctor">
                      <FaUserMd /> {record.doctor}
                    </p>
                  </div>
                </div>
                <p className="history-notes">{record.notes}</p>
                {record.documents.length > 0 && (
                  <div className="history-documents">
                    <strong>Documents:</strong>
                    <div className="document-list">
                      {record.documents.map((doc, i) => (
                        <button key={i} className="document-btn">
                          <FaFileMedical />
                          {doc}
                        </button>
                      ))}
                    </div>
                  </div>
                )}
              </div>
            </motion.div>
          ))}
        </div>
      </div>
    </PatientLayout>
  );
};

export default PatientHistory;
